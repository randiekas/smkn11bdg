							<a href="#" class="h4">Pengumuman Aksi Solidaritas Dana Bantuan Bencana Di Jawa Barat</a> 
							<small class="block m-t-xs"><p>Bandung, 03 Februari 2014</p>
<p>Kepada,<br>Yth. Ibu/Bapak/Orang Tua/Wali Siswa<br>dan Guru/Karyawan SMK Negeri 11 Banduung</p>
<p>Assalamu'alaikum Wr. Wb.<br>Sebagaimana dimaklumi bersama, bahwa di Jawa Barat telah terjadi berbagai bencana alam yang menimbulkan korban baik harta maupun jiwa manusia. SMK Negeri 11 Bandung sebagai bagian dari warga kota Bandung yang berada dilingkungan Jawa Barat merasa perlu turut membantu meringankan beban korban bencana tersebut dan memberikan pembelajaran &nbsp;kepada peserta didik (siswa) tentang pentingnya memiliki rasa empati, peduli terhadap orang lain dan ikut serta dalam penggalangan bencana.</p>
<p>Menindaklanjuti hal tersebut, SMK Negeri 11 Bandung melakukan kegiatan "Aksi SOolidaritas Pengumpulan Bantuan Bencana Di Jawa Barat" dengan menggalang dana berupa uag dari Ibu/Bapak/Orang Tua/Wali, Siswa dan Guru/Karyawan untuk disalurkan kepada bencana alam.</p>
<p>Harapan kami, Ibu/Bapak/Orang Tua/Wali, Siswa dan Guru/Karyawan dapat ikut serta berpartisipasi dengan memberikan sedikit rizki melalui aksi tersebut. (Bukti partisipasi terlampir).</p>
<p>Demikian surat pemberitahuan ini kami sampaikan, atas perhatiannya kami ucapkan terima kasih.</p>
<p>Wassalamu'alaikum Wr. Wb.</p>
